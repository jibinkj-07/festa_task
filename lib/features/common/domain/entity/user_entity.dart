class UserEntity {
  final String title;
  final String firstName;
  final String lastName;
  final String gender;
  final String email;
  final DateTime dob;
  final String phone;
  final String address;
  final String imageUrl;

  UserEntity({
    required this.title,
    required this.firstName,
    required this.lastName,
    required this.gender,
    required this.email,
    required this.dob,
    required this.phone,
    required this.address,
    required this.imageUrl,
  });
}
